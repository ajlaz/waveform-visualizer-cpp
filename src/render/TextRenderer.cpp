bool TextRenderer::init(const std::string& shaderDir) {
    if (!shader_.load(shaderDir + "/text.vert", shaderDir + "/text.frag"))
        return false;

    buildAtlas();

    // VAO / VBO for dynamic quads
    glGenVertexArrays(1, &vao_);
    glGenBuffers(1, &vbo_);
    glBindVertexArray(vao_);
    glBindBuffer(GL_ARRAY_BUFFER, vbo_);

    // layout: vec2 pos, vec2 uv
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float),
                          reinterpret_cast<void*>(0));
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float),
                          reinterpret_cast<void*>(2 * sizeof(float)));

    glBindVertexArray(0);
    glBindBuffer(GL_ARRAY_BUFFER, 0);
    return true;
}

void TextRenderer::buildAtlas() {
    // Atlas: kNumChars*kCharW wide, kCharH tall, GL_R8
    // Row 0 of kFont = top row of glyph → stored at texture y=0 (GL bottom)
    // UV mapping in draw(): screen-top → v=0, screen-bottom → v=1 (inverted v)
    const int W = kNumChars * kCharW;
    const int H = kCharH;
    std::vector<uint8_t> atlas(W * H, 0);

    for (int c = 0; c < kNumChars; ++c) {
        for (int row = 0; row < kCharH; ++row) {
            uint8_t bits = kFont[c][row];
            for (int col = 0; col < kCharW; ++col) {
                bool on = (bits >> (7 - col)) & 1;
                atlas[row * W + c * kCharW + col] = on ? 255u : 0u;
            }
        }
    }

    glGenTextures(1, &fontTex_);
    glBindTexture(GL_TEXTURE_2D, fontTex_);
    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_R8, W, H, 0,
                 GL_RED, GL_UNSIGNED_BYTE, atlas.data());
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glBindTexture(GL_TEXTURE_2D, 0);
}

void TextRenderer::resize(int vpW, int vpH) {
    vpW_ = std::max(vpW, 1);
    vpH_ = std::max(vpH, 1);
}

void TextRenderer::draw(const std::string& text, int x, int y,
                        float r, float g, float b, float scale) const {
    if (text.empty() || vpW_ <= 0 || vpH_ <= 0) return;

    const float cw = kCharW  * scale;
    const float ch = kCharH  * scale;

    // 6 vertices per glyph, each = (ndcX, ndcY, u, v)
    std::vector<float> verts;
    verts.reserve(text.size() * 6 * 4);

    for (size_t i = 0; i < text.size(); ++i) {
        int idx = static_cast<unsigned char>(text[i]) - 32;
        if (idx < 0 || idx >= kNumChars) idx = 0;

        // Pixel coords of the glyph quad (top-left origin, y-down)
        float px0 = x + i * cw;
        float px1 = px0 + cw;
        float py0 = static_cast<float>(y);       // top
        float py1 = py0 + ch;                    // bottom

        // Convert to NDC: x in [-1,1], y in [-1,1] with +y = up
        float nx0 =  px0 / vpW_ * 2.0f - 1.0f;
        float nx1 =  px1 / vpW_ * 2.0f - 1.0f;
        float ny0 = -py0 / vpH_ * 2.0f + 1.0f;  // screen top  → higher NDC y
        float ny1 = -py1 / vpH_ * 2.0f + 1.0f;  // screen bottom → lower NDC y

        // UV: character occupies u=[idx/N, (idx+1)/N], v=[0,1]
        // Row 0 of font is at texture y=0 (v=0 in GL = bottom of tex).
        // We want screen-top (ny0) to show font row 0 → ny0 maps to v=0.
        float u0 = static_cast<float>(idx)     / kNumChars;
        float u1 = static_cast<float>(idx + 1) / kNumChars;
        float vt = 0.0f;   // font top row (row 0) → v=0
        float vb = 1.0f;   // font bottom row (row 7) → v=1

        // Triangle 1: TL, BL, TR
        verts.insert(verts.end(), {nx0,ny0, u0,vt});
        verts.insert(verts.end(), {nx0,ny1, u0,vb});
        verts.insert(verts.end(), {nx1,ny0, u1,vt});
        // Triangle 2: BL, BR, TR
        verts.insert(verts.end(), {nx0,ny1, u0,vb});
        verts.insert(verts.end(), {nx1,ny1, u1,vb});
        verts.insert(verts.end(), {nx1,ny0, u1,vt});
    }

    // Save state
    GLint prevVAO = 0, prevProg = 0;
    glGetIntegerv(GL_VERTEX_ARRAY_BINDING, &prevVAO);
    glGetIntegerv(GL_CURRENT_PROGRAM,      &prevProg);

    // Upload & draw
    glBindVertexArray(vao_);
    glBindBuffer(GL_ARRAY_BUFFER, vbo_);
    glBufferData(GL_ARRAY_BUFFER,
                 static_cast<GLsizeiptr>(verts.size() * sizeof(float)),
                 verts.data(), GL_STREAM_DRAW);

    shader_.use();
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, fontTex_);
    shader_.setInt("uFont",  0);
    shader_.setVec3("uColor", r, g, b);

    glDrawArrays(GL_TRIANGLES, 0, static_cast<GLsizei>(verts.size() / 4));

    // Restore
    glBindVertexArray(static_cast<GLuint>(prevVAO));
    glUseProgram(static_cast<GLuint>(prevProg));
}

void TextRenderer::drawCentered(const std::string& text, int cx, int y,
                                 float r, float g, float b, float scale) const {
    int textW = static_cast<int>(text.size() * kCharW * scale);
    draw(text, cx - textW / 2, y, r, g, b, scale);
}

TextRenderer::~TextRenderer() {
    if (fontTex_) { glDeleteTextures(1, &fontTex_);      fontTex_ = 0; }
    if (vbo_)     { glDeleteBuffers(1, &vbo_);            vbo_ = 0; }
    if (vao_)     { glDeleteVertexArrays(1, &vao_);       vao_ = 0; }
}
